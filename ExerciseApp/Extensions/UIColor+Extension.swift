import UIKit

extension UIColor {
    @nonobjc class var cerulean: UIColor {
        return UIColor(red: 57.0 / 255.0, green: 65.0 / 255.0, blue: 80.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightGreen: UIColor {
        return UIColor(red: 147.0 / 255.0, green: 219.0 / 255.0, blue: 210.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkBlue: UIColor {
        return UIColor(red: 88.0 / 255.0, green: 177.0 / 255.0, blue: 205.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightRed: UIColor {
        return UIColor(red: 250.0 / 255.0, green: 100.0 / 255.0, blue: 72.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightBlue: UIColor {
        return UIColor(red: 26.0 / 255.0, green: 131.0 / 255.0, blue: 228.0 / 255.0, alpha: 1.0)
    }
}
